  `bnr_img_width` int(11) default NULL,
  `bnr_img_height` int(11) default NULL,
  `bnr_img_max_width` int(11) default '800',
  `bnr_image` varchar(255) default NULL,
  `bnr_usr_id` mediumint(9) NOT NULL default '0',
  `bnr_own_id` mediumint(9) default NULL,
  `bnr_create` datetime default NULL,
