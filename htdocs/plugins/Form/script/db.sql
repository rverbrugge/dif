CREATE TABLE IF NOT EXISTS `form_record_item` (
  `item_id` mediumint(9) NOT NULL auto_increment,
  `item_rcd_id` mediumint(9) NOT NULL default '0',
  `item_elm_id` varchar(15) default '',
  `item_classname` varchar(25) default '',
  `item_weight` int(11) default '1',
  `item_name` varchar(100) default NULL,
  `item_value` text,
  PRIMARY KEY  (`item_id`),
  KEY `idx_item_tree_id` (`item_rcd_id`),
  KEY `item_rcd_id` (`item_rcd_id`)
) ENGINE=MyISAM AUTO_INCREMENT=540 DEFAULT CHARSET=latin1;
CREATE TABLE IF NOT EXISTS `form_settings` (
