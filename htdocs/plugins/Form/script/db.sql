  `form_to` varchar(100) default NULL,
  `form_caption` varchar(50) default '',
  `form_action` int(11) default '1',
  `form_mail_text` text,
  `form_templatefield` text,
  `form_mandatorysign` varchar(100) default NULL,
  `form_usr_id` int(11) default NULL,
  `form_own_id` int(11) default NULL,
