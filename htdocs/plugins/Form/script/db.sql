CREATE TABLE IF NOT EXISTS `form` (
  `form_id` mediumint(8) NOT NULL auto_increment,
  `form_tree_id` mediumint(9) NOT NULL default '0',
  `form_tag` varchar(100) default NULL,
  `form_type` varchar(25) NOT NULL,
  `form_active` tinyint(1) default '1',
  `form_weight` int(11) default '0',
  `form_mandatory` tinyint(1) default '1',
  `form_size` int(11) default '1',
  `form_name` varchar(255) default NULL,
  `form_description` text,
  `form_default` varchar(255) default NULL,
  `form_options` text,
  `form_usr_id` mediumint(9) NOT NULL default '0',
  `form_own_id` mediumint(9) default NULL,
  `form_create` datetime default NULL,
  `form_ts` timestamp NOT NULL default CURRENT_TIMESTAMP on update CURRENT_TIMESTAMP,
  PRIMARY KEY  (`form_id`),
  KEY `idx_form_tree_id` (`form_tree_id`),
  KEY `idx_form_tag` (`form_tag`),
  KEY `form_tree_id` (`form_tree_id`,`form_tag`,`form_active`)
) ENGINE=MyISAM AUTO_INCREMENT=75 DEFAULT CHARSET=latin1;
